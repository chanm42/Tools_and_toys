def pretty_print(M):
##=====================================================================================##
    nrow,ncol = M.shape
    for row in range(nrow):
        row_str = np.zeros(ncol)
        for col in range(ncol):
            x            = M[row,col]
            row_str[col] = str(x)
            row_out      = "\t".join(row_str)
        print(row_out)
    return None

