def infile_reader(infile):
##=====================================================================================##
    xvec = []
    yvec = []
    with open(infile,"r") as f:
        for line in f:
            cols = line.split(line)
            xvec.append(float(cols[0]))
            yvec.append(float(cols[1]))
    return xvec,yvec

